/**
 * 音声合成用にマークダウンの記号を除去し, 読み上げて自然なテキストへ変換する.
 * 応答は画面にマークダウンとして整形表示される一方, TTSには本文だけを渡す
 * （表示・履歴は原文のまま保持する）.
 */
export function stripMarkdown(text: string): string {
  let t = text;
  // コードフェンス（```lang）: フェンス行だけ落とし, 中身は読む.
  t = t.replace(/^\s*(```|~~~)[^\n]*$/gm, "");
  // 画像 → 代替テキスト, リンク → 表示名.
  t = t.replace(/!\[([^\]]*)\]\([^)]*\)/g, "$1");
  t = t.replace(/\[([^\]]+)\]\([^)]*\)/g, "$1");
  // 生URLは読み上げない.
  t = t.replace(/https?:\/\/[^\s)>」』】]+/g, "");
  // 見出し・引用・箇条書き・番号リストの行頭記号.
  t = t.replace(/^#{1,6}\s+/gm, "");
  t = t.replace(/^>\s?/gm, "");
  t = t.replace(/^\s*[-*+]\s+/gm, "");
  t = t.replace(/^\s*\d+[.)]\s+/gm, "");
  // 表: 罫線行（|---|---|）を除去し, セル区切りを読点にする.
  t = t.replace(/^\s*\|?[\s:|-]*-[\s:|-]*\|?\s*$/gm, "");
  t = t.replace(/^\s*\|\s*/gm, "").replace(/\s*\|\s*$/gm, "");
  t = t.replace(/\s*\|\s*/g, "，");
  // 水平線.
  t = t.replace(/^\s*([-*_]\s*){3,}$/gm, "");
  // 強調・打ち消し・インラインコード.
  t = t.replace(/(\*\*\*|___)(.+?)\1/g, "$2");
  t = t.replace(/(\*\*|__)(.+?)\1/g, "$2");
  t = t.replace(/(\*|_)([^*_\n]+)\1/g, "$2");
  t = t.replace(/~~(.+?)~~/g, "$1");
  t = t.replace(/`([^`\n]*)`/g, "$1");
  // 空行・連続空白を整える.
  t = t.replace(/[ \t]{2,}/g, " ").replace(/\n{3,}/g, "\n\n");
  return t;
}
